class FortuneData {
  const FortuneData({
    required this.queName,
    required this.queSymbol,
    required this.fortuneLevel,
    required this.verse,
    required this.dish,
    required this.dishMeaning,
    required this.imageUrl,
    required this.advice,
    required this.stickNumber,
  });

  final String queName;
  final String queSymbol;
  final String fortuneLevel;
  final String verse;
  final String dish;
  final String dishMeaning;
  final String imageUrl;
  final String advice;
  final String stickNumber;
}
